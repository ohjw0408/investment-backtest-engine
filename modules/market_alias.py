def _norm(s: str) -> str:
    """소문자 + 영숫자·한글만 남김 (띄어쓰기·/·& 등 제거)."""
    return re.sub(r"[^0-9a-z가-힣]", "", (s or "").lower())


# code → {name, country}: /symbol 상세 페이지가 지수·금리 등 비상장 코드의 표시명·통화에 사용
MARKET_CODE_META = {
    code: {"name": name, "country": country}
    for code, name, badge, country, aliases in MARKET_ITEMS
}

# 사전 계산: 각 item의 매칭 토큰
_INDEXED = [
    {
        "code": code, "name": name, "badge": badge, "country": country,
        "tokens": [_norm(code), _norm(name)] + [_norm(a) for a in aliases],
    }
    for code, name, badge, country, aliases in MARKET_ITEMS
]


def search_market_aliases(q: str, limit: int = 8) -> list:
    """질의와 매칭되는 시장 심볼(지수·환율·원자재·금리) 반환."""
    qn = _norm(q)
    if not qn:
        return []
    out = []
    for it in _INDEXED:
        # 토큰(코드·한글명·별칭)이 질의로 시작하면 매칭. 부분매칭은 오검색(금⊂금리) 유발해 제외.
        if any(tok.startswith(qn) for tok in it["tokens"] if tok):
            out.append({
                "code": it["code"], "name": it["name"], "badge": it["badge"],
                "subtitle": "", "country": it["country"], "is_etf": False,
            })
            if len(out) >= limit:
                break
    return out
